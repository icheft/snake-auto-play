void Map::showMap()
{
    const int spaceSize = 2;
    cout << " Map size: " << this->map.size() << " x " << this->map[0].size() << endl;
    for (int i = 0; i < this->map.size(); i++) {
        for (int j = 0; j < this->map[0].size(); j++) {
            if (i == get<0>(this->headPos) && j == get<1>(this->headPos)) {
                cout << right << string(spaceSize, ' ') << RED << "x" << RESET;
            } else if (i == get<0>(this->tailPos) && j == get<1>(this->tailPos)) {
                cout << right << string(spaceSize, ' ') << BLUE << "x" << RESET;
            } else if (this->map[i][j] == -3) {
                cout << right << string(spaceSize, ' ') << GREEN << "x" << RESET;
                // REF: [map change]
            } else if (this->map[i][j] == -1) {
                cout << right << string(spaceSize - 1, ' ') << BOLDBLACK << this->map[i][j] << RESET;
            } else if (this->map[i][j] != 0) {
                cout << right << string(spaceSize, ' ') << BOLDYELLOW << this->map[i][j] << RESET;
            } else
                cout << right << string(spaceSize, ' ') << this->map[i][j];
        }
        cout << endl;
    }
    return;
}

bool Map::hasPoint()
{
    for (int i = 0; i < map.size(); i++) {
        for (int j = 0; j < map[0].size(); j++) {
            if (map[i][j] != 0 && map[i][j] != -3 && map[i][j] != -1) return true;
        }
    }
    return false;
}

